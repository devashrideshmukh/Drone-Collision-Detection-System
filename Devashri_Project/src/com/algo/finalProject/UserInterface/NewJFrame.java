    public class Task extends TimerTask {

        @Override
        public void run() {
            //deleting previous anomalies every second
            utility.deleteAnomalies(componentNodeMap);

            //creating random anomalies
            int numberOfAnnomalies = utility.createAnomalies(componentNodeMap);
            numberOfAnomalies.setText(String.valueOf(numberOfAnnomalies));

            //setting colors to every node depending on its anomalies
            for (Node node : componentNodeMap.values()) {

                StringBuilder sb = new StringBuilder();
                sb.append(node.getxCoOrdinate());
                sb.append(node.getyCoOrdinate());
                String s = sb.toString();
                JLabel jlabel = (JLabel) getComponentByName(s);

                if (node.isHighIntensityAnomalyPresent()) {
                    //if high intensity anomaly is present
                    jlabel.setForeground(Color.red);

                } else if (node.isLowIntensityAnomalyPresent()) {
                    //if low intensity annomaly is present
                    jlabel.setForeground(Color.ORANGE);

                } else {
                    //no anomaly
                    jlabel.setForeground(Color.black);
                }

                if (node.isVisited()) {
                    //traversing path
                    jlabel.setForeground(Color.WHITE);
                }
            }

            //flag to indicate if the source has reached the destination
            boolean flag = false;
            if (targetPath == null) // if there is no entry in the machine for the corresponding source, then start calculating path using nearest neighbor chaining
            {
                System.err.println("=====================Learning Path by Nearest Neighbour Chaining===========================");
                flag = drone.fly();
                String nodeToBeColored = drone.getCurrentNode().getNodeName();
                JLabel jlabel = (JLabel) getComponentByName(nodeToBeColored);
                jlabel.setForeground(Color.GREEN);

                //this arraylist is used to add nodes that have been traversed
                traversedPath.addNodeToPathNode(drone.getCurrentNode());
                if (!smallPaths.isEmpty()) // if any small path exists
                {
                    for (PathNode p : smallPaths) {
                        p.addNodeToPathNode(drone.getCurrentNode());
                    }
                }

                System.out.println("Nearest node: (Selected Node) " + drone.getCurrentNode().toString());
            } // nearest neighbour chaining gets over 
            else {

                Node nodeToTravel = targetPath.getListOfNodes().get(taskCount); //get single node from search path
                if (nodeToTravel.isHighIntensityAnomalyPresent()) // anomaly at search path 
                {

                    System.out.println(ANSI_PURPLE + "Anomaly arrived while learning the path:" + nodeToTravel.getNodeName() + ANSI_RESET);
                    flag = drone.fly();
                    String nodeToBeColored = drone.getCurrentNode().getNodeName();
                    JLabel jlabel = (JLabel) getComponentByName(nodeToBeColored);
                    jlabel.setForeground(Color.GREEN);
                    traversedPath.addNodeToPathNode(drone.getCurrentNode());

                    if (!smallPaths.isEmpty()) // if any small path exist
                    {
                        for (PathNode p : smallPaths) {
                            p.addNodeToPathNode(drone.getCurrentNode());
                        }
                    }

                    System.out.println("Nearest node (Selected Node) : " + drone.getCurrentNode().toString());
                    targetPath = getPathFromNewSource(drone.getCurrentNode()); //retrieving learned path from node at which anomaly occured
                    if (targetPath == null) // if no path available we have to add small path
                    {
                        PathNode small = new PathNode();
                        small.addNodeToPathNode(drone.getCurrentNode());
                        Link value = new Link();
                        value.first = small;
                        machine.getDataSet().put(drone.getCurrentNode(), value);
                        smallPaths.add(small);
                        smallPathCount++;
                    }
                    insertpath = true;
                    taskCount = 0;
                } else {
                    // travelling via trained path
                    drone.setCurrentNode(nodeToTravel);
                    traversedPath.addNodeToPathNode(nodeToTravel);
                    if (!smallPaths.isEmpty()) {
                        for (PathNode p : smallPaths) {
                            p.addNodeToPathNode(nodeToTravel);
                        }
                    }
                    JLabel jlabel = nodeToTravel.getLabel();
                    jlabel.setForeground(Color.GREEN);
                    taskCount++;
                    insertpath = false;
                    System.out.println("Using learned path to travel:  " + nodeToTravel.toString());

                    if (nodeToTravel.equals(drone.getDestination())) {
                        flag = true;
                        System.out.println("CHECK" + insertpath);
                    }
                }

            }

            if (flag) {
                if (!insertpath) // we dont have to insert any path as travelled via trained path
                {
                    System.out.println("Drone travelled via previously trained path: " + targetPath);
                    targetPath.setTraversalCount(targetPath.getTraversalCount() + 1);
                    System.out.println("Sorting linked list to accomodate change in Traversal Count");
                    Link linkToBeSorted = machine.getDataSet().get(drone.getSource());
                    System.out.println("Search path" + targetPath.toString());

                    try {
                        linkToBeSorted.sortLinkedListByTraversalCount(targetPath);
                        System.out.println(ANSI_PURPLE+"Linked list has been sorted successfully to accomodate change in Traversal Count"+ANSI_PURPLE);
                    } catch (Exception e) {
                        System.out.println("Data managment issue");
                    }

                   
                } else { // adding path
                    System.out.println("Drone has not travelled via learned path: ");
                    traversedPath.setTraversalCount(traversedPath.getTraversalCount() + 1);

                    if (!smallPaths.isEmpty()) {
                        for (PathNode p : smallPaths) {
                            p.setTraversalCount(1);
                        }
                    }

                    if (machine.getDataSet().containsKey(drone.getSource())) {
                        System.out.println("Adding new path to linked list");
                        machine.getDataSet().get(drone.getSource()).insertPathNode(traversedPath);

                    } else {
                        System.out.println("Adding new source to hashtable");
                        Link value = new Link();
                        value.first = traversedPath;
                        machine.getDataSet().put(drone.getSource(), value);
                    }

                }
                System.err.println(ANSI_BLUE + "=====================================================================================");
                System.err.println("DRONE HAS REACHED ITS DESTINATION");
                System.err.println("=====================================================================================" + ANSI_RESET);
                long timeEnd = System.currentTimeMillis();
                System.out.println("Time taken" + (timeEnd - timeStart));
                timer.cancel();
            }

            float fuel = drone.getFuel();

            drone.getCurrentNode().setVisited(true);
            if (drone.getCurrentNode().isLowIntensityAnomalyPresent()) {
                drone.setFuel(fuel - 2);

            } else {
                drone.setFuel(fuel - 1);
            }
            fuelBar.setValue((int) drone.getFuel());
            fuelRemaining.setText(String.valueOf(drone.getFuel()));

        }
    }

    public PathNode getPathFromNewSource(Node nodeSource) {

        if (machine.getDataSet().isEmpty() || !machine.getDataSet().containsKey(nodeSource)) {
           
            insertpath = true;
            return null;

        }

        return machine.getDataSet().get(nodeSource).first;

    }


